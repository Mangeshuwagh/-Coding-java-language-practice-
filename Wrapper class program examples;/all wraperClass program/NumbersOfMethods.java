import java.util.Scanner;

public class NumbersOfMethods {

    public static void main(String[] args) {
        // System.out.println("Hello dear Damini");

        Float f = 45.7f;
        Double d = 72.9;
        Long l = 188282L;

        // numbers of methods:

        long a = f.longValue();
        long b = d.longValue();
        long c = l.longValue();

        System.out.println(" Float value converted by Long: " + a);
        System.out.println(" Double value converted by Long: " + b);
        System.out.println(" Long value converted by Long: " + c);

    }

}
